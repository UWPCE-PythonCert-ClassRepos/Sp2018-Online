"""
This module provides a addition operator
"""
class Adder(object):
    """ adder class """

    @staticmethod
    def calc(operand_1, operand_2):
        """
        :param operand_1: variable 1
        :param operand_2: variable 2
        :return: runs the sum of operand 1 and 2
        """
        return operand_1 + operand_2
