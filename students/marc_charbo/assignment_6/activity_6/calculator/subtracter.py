"""
This module provides a subtraction operator
"""
class Subtracter(object):
    """ substracter class """
    @staticmethod
    def calc(operand_1, operand_2):
        """
        :param operand_1: variable 1
        :param operand_2: variable 2
        :return: returns substraction variable 1 - variable 2
        """
        return operand_1 - operand_2
